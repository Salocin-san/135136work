//prints biggest integer out of 3 integers

#include <iostream>

using namespace std;

int max2(int a, int b){
    
    if (a > b){
        return a;
    }
    
    else{
        return b;
    }
}

int max3(int a, int b, int c){
    return max2(max2(a,b),c);
}

int main()
{
    int a,b,c;
    cout << "Enter three numbers: ";
    cin >> a >> b >> c;

    cout << max3 (a,b,c) << endl;
        
    return 0;
}
